/***************************************************************************************************
* USART1 调试串口（USB转串口）
*
* 接线说明：
*   PA9   (TX)  --->  RXD
*   PA10  (RX)  <---  TXD
*   GND         --->  GND
*
* 注意事项：
*   1. TX 与 RX 必须交叉连接
*
***************************************************************************************************/

/***************************************************************************************************
* USART3 蓝牙模块（HC-05）
*
* 接线说明：
*   PB10  (TX)  --->  RXD
*   PB11  (RX)  <---  TXD
*   5V          --->  VCC
*   GND         --->  GND
*
* 注意事项：
*   1. TX 与 RX 必须交叉连接
*   2. 建议使用独立 5V 电源供电（避免供电不稳）
*
***************************************************************************************************/

/***************************************************************************************************
* USART2 WiFi模块（ESP-01S）
*
* 接线说明：
*   PA2   (TX)  --->  RX
*   PA3   (RX)  <---  TX
*   3.3V        --->  VCC
*   GND         --->  GND
*   3.3V        --->  EN
*   3.3V        --->  RST
*
* 注意事项：
*   1. 必须使用 3.3V 供电（严禁 5V）
*   2. 建议使用独立 3.3V 电源（WiFi瞬时电流较大）
*   3. EN、RST 必须保持高电平
*
***************************************************************************************************/

#include "common.h"

int main(void) {
    /* 系统初始化 */
    System_Init();

    /* 创建 WiFi 配置任务，优先级为2 */
    BaseType_t xRet = xTaskCreate(WiFiConfigTask_Entry,
                                  "WiFiCfgTask",
                                  configMINIMAL_STACK_SIZE,
                                  NULL,
                                  tskIDLE_PRIORITY + 2,
                                  &WiFiConfigTaskHandle);

    /* 判断任务是否创建成功 */
    if (xRet == pdPASS) {
        printf1("WiFiConfigTask Create OK\r\n");
    } else {
        printf1("WiFiConfigTask Create Error\r\n");
    }
	
	#if ( configENABLE_DEBUG_STACK_MONITOR == 1 )
	MonitorTask_Create();
	#endif 

    /* 启动任务调度器 */
    vTaskStartScheduler();

    /* 正常情况下不会运行到这里 */
    while (1) {
    }
}
